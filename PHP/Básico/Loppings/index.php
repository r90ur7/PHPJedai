<?php
//Loopings
//for
for($count=0;$count<11;$count++){
    echo $count;
    echo "<br>";
}
echo "<br/>";

//while

$contador = 0;
while($contador<11){
    echo $contador;
    echo "<br>";
    $contador++;
}

echo "<br/>";

//do while

$contador = 0;
do{
    echo $contador;
    echo "<br>";
    $contador++;
}while($contador<11);

?>